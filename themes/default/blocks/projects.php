                                <div class="modal-body text-start pe-3"><h3 class="text-start text-secondary mb-3">Site
                                        de location de voiture</h3>
                                    <p>Le projet consiste en la réalisation d'un site internet accessible pour les
                                        clients, qui propose un catalogue de voitures et la possibilité de réserver ces
                                        dernières. Pour assurer la gestion de l'ensemble de ces fonctionnalités, un
                                        panel administration a été mis en place.</p>
                                    <p>Le panel administration permet aux administrateurs de gérer le catalogue de
                                        voitures, les comptes administrateurs, les clients, les messages et les demandes
                                        de réservation. Cela permet une gestion efficace et centralisée de toutes les
                                        activités liées à la location de voitures.</p>
                                    <p>Afin de communiquer avec la base de données, une API REST a été mise en place.
                                        Cette API permet de récupérer des informations sur les voitures disponibles, les
                                        réservations en cours, les clients, etc. Elle permet également de mettre à jour
                                        ces informations en temps réel.</p>
                                    <p>Pour permettre aux clients de payer leur réservation en ligne, un système de
                                        paiement a été intégré avec Stripe. Cela permet aux clients de payer leur
                                        réservation de manière sécurisée et rapide, sans avoir à se déplacer.</p>
                                    <p>Enfin, pour offrir un service de communication en temps réel aux clients, un
                                        chatbox a été intégré à l'aide de l'outil Tawk.to. Cela permet aux clients de
                                        poser des questions ou de demander des informations à tout moment, et aux
                                        administrateurs de répondre rapidement.</p>
                                    <p>Dans l'ensemble, ce projet a permis de développer un site internet complet et
                                        efficace pour la location de voitures, en offrant une expérience utilisateur de
                                        qualité, tout en facilitant la gestion pour les administrateurs.</p>
